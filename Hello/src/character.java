public class Character {
    public static void main(String[] args) {
        
        // 문자 타입 - 2byte
        // 문자를 넣기 위한 데이터타입(문자열이 아님)
        // 문자는 홑따옴표('')로 감싸서 리터럴을 만든다.
        char ch1 = 'A';
        char ch2 = '박';
        System.out.println(ch1);
        System.out.println(ch2);

        // 문자열 타입
        // string은 데이터타입이 아니고 클래스이다.(Non-primitive Type)
        String str = "apple";
        System.out.println(str);

    }
}
