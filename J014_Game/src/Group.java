public class Group {
    
    private int terran;
    private int protoss;
    private int zerg;

    public Group() {
        terran = 0;
        protoss = 0;
        zerg = 0;
    }

    public Group(int terran, int protoss, int zerg) {
        this.terran = terran;
        this.protoss = protoss;
        this.zerg = zerg;
    }
}
