package L01_Original;
public class Lotto {

    private int lottoNum;

    public Lotto() {
        lottoNum = 0;
    }

    public Lotto(int lottoNum) {
        this.lottoNum = lottoNum;
    }

    public void setLottoNum(int lottoNum) {
        this.lottoNum = lottoNum;
    }

    public int lottoNum() {
        return lottoNum;
    }
}
